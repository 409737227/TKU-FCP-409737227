public class hm_03 {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        double z = 5.5;
        
        // 計算(x + y) * z 的結果
        double result = (x + y) * z;
        
        // 打印計算結果
        System.out.println("(x + y) * z =" + result);
    }
}
