public class hm_01 {
    public static void main(String[] args) {
        // 宣告並初始化整數變數age，值為25
        int age = 25;
        
        // 宣告並初始化字元變數initial，值為'A'
        char initial = 'A';
        
        // 宣告並初始化字串變數name，值為"John"
        String name = "John";
        
        // 打印每個變數的值
        System.out.println("age: " + age);
        System.out.println("initial: " + initial);
        System.out.println("name: " + name);
    }
    
    
}
